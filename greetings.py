# Datatypes and Variables

greeting = 'Hello World'
greeting = 'Hello'
print(greeting)

myStr = 'Hello'
myInt = 58
myFloat = 58.58
myList = [4, 5, 'Fuck']
myDict = {'a': 1, 'b': [4, 5]}

print(type(myStr), myStr + ' World')
print(type(myInt), myInt + 5)
print(type(myFloat), myFloat)
print(type(myList), myList)
print(type(myDict), myDict)
